private void updateRestaurantList() {
    listModel.clear();

    for (int i = 0; i < 10; i++) {
        boolean meetsCriteria = true;

        if (jRadioButton1.isSelected() && !DateCity.vecRestaurant.get(i).animaldecompanie) {
            meetsCriteria = false;
        }

        if (jRadioButton2.isSelected() && !DateCity.vecRestaurant.get(i).persoanefumatoare) {
            meetsCriteria = false;
        }

        if (jRadioButton3.isSelected() && !DateCity.vecRestaurant.get(i).terasa) {
            meetsCriteria = false;
        }
        if (jRadioButton4.isSelected() && !DateCity.vecRestaurant.get(i).codvestimentar) {
            meetsCriteria = false;
        }

        if (meetsCriteria) {
            listModel.addElement(DateCity.vecRestaurant.get(i).nume);
        }
    }
}
