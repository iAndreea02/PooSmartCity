private void calculeazaPretComanda() {
      boolean isJRadioButton5Selected = jRadioButton5.isSelected();
        boolean isJRadioButton6Selected = jRadioButton6.isSelected();
          boolean isJRadioButton7Selected = jRadioButton7.isSelected();
           boolean isJRadioButton8Selected = jRadioButton8.isSelected();
            boolean isJRadioButton9Selected = jRadioButton9.isSelected();
        pret=0;
    for(int i=0;i<10;i++)
    {
    if(valoare==DateCity.vecRestaurant.get(i).nume)
    {
     if (isJRadioButton5Selected) {
            pret+= pretmeniulzilei;
        }

        if (isJRadioButton7Selected) {
            pret += DateCity.vecRestaurant.get(i).getPret()[0];
        }
 if (isJRadioButton8Selected) {
            pret += DateCity.vecRestaurant.get(i).getPret()[1];
        }
    if (isJRadioButton9Selected) {
            pret += DateCity.vecRestaurant.get(i).getPret()[2];
        }
    if (isJRadioButton6Selected) {
            pret += DateCity.vecRestaurant.get(i).getPret()[3];
        }
    }
    }
    jTextArea1.setText(" comanda a fost efectuata si acesta este pretul  "+pret);

   Factura();
   MenuPrincipal.setBill(pret);
}
